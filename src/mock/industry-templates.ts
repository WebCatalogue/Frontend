function tpl(
  industrySlug: string,
  id: string,
  name: string,
  description: string,
  themeId: string,
  paletteId: string,
  animationStyle: AnimationStyle,
  buttonStyle: ButtonStyle,
  borderRadius: string,
  supportsDarkMode: boolean,
  pages: string[],
  sections: string[],
  imageKey: Parameters<typeof demoImage>[0],
): IndustryTemplate {
  return {
    id,
    industrySlug,
    name,
    description,
    themeId,
    paletteId,
    animationStyle,
    buttonStyle,
    borderRadius,
    supportsDarkMode,
    pages,
    sections,
    desktopImage: demoImage(imageKey, 1400),
    mobileImage: demoImage(imageKey, 600),
  };
}

